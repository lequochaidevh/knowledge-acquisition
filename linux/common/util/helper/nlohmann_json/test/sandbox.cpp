bool load_base_schema(const std::string& file_path, nlohmann::json& root) {
    // Re-use our safe load function built previously
    if (!JsonHelper::load_from_file(file_path, root)) {
        return false;
    }

    // Structural validation: Ensure the base file has the critical keys we expect
    if (!root.contains("username") || !root.contains("login_count")) {
        std::cerr << "[JsonHelper] Base file validation failed: Missing vital keys." << std::endl;
        return false;
    }

    return true;
}

/**
 * @test Verify appending elements to the end, front, and middle of a JSON Array.
 */
TEST(JsonHelperMutationTest, ArrayAppendAndInsertBehaviors) {
    // 1. ARRANGE: Create a starting JSON object with a basic array
    nlohmann::json root;
    load_base_schema("base_config.json", root);
    root["languages"] = nlohmann::json::array({"C++", "Python"});

    // 2. ACT & ASSERT: Scenario A - Append to the END (Back)
    bool append_back_ok = JsonHelper::append_back(root, "languages", "Go");
    ASSERT_TRUE(append_back_ok);
    EXPECT_EQ(root["languages"].size(), 3);
    EXPECT_EQ(root["languages"].back().get<std::string>(), "Go");

    JsonHelper::save_to_file("append_back_alphabet.json", root);
    JsonHelper::save_to_file_ordered("append_back_ordered.json", root);

    // Scenario B: Append to the START (Front)
    bool append_front_ok = JsonHelper::append_front(root, "languages", "C#");
    ASSERT_TRUE(append_front_ok);
    EXPECT_EQ(root["languages"].size(), 4);
    EXPECT_EQ(root["languages"].front().get<std::string>(), "C#");  // First element should be C#

    JsonHelper::save_to_file("append_front.json", root, true);

    // Scenario C: Insert into the MIDDLE (Index 2)
    // Current layout: ["C#", "C++", "Python", "Go"]
    bool insert_mid_ok = JsonHelper::insert_at(root, "languages", 2, "Rust");
    ASSERT_TRUE(insert_mid_ok);
    EXPECT_EQ(root["languages"].size(), 5);
    EXPECT_EQ(root["languages"][2].get<std::string>(), "Rust");  // ["C#", "C++", "Rust", "Python", "Go"]

    JsonHelper::save_to_file("insert_mid.json", root, true);
}

/**
 * @test Verify error handling when passing wrong data types or out-of-bound indices.
 */
TEST(JsonHelperMutationTest, HandlesMutationErrorsGracefully) {
    // 1. ARRANGE: Set up a node containing a non-array target and a good array
    nlohmann::json root;
    root["not_an_array"] = "I am a pure string";
    root["items"]        = {"apple", "banana"};

    // 2. ACT & ASSERT: Scenario A - Trying to append to a string key (Should fail)
    bool fail_append = JsonHelper::append_back(root, "not_an_array", "orange");
    EXPECT_FALSE(fail_append) << "Should return false because target key is not a JSON Array";

    // Scenario B: Trying to insert front on a non-existent key (Should fail)
    bool fail_front = JsonHelper::append_front(root, "ghost_key", "value");
    EXPECT_FALSE(fail_front);

    // Scenario C: Inserting at an out-of-bounds index (Index 10 on a size 2 array)
    bool fail_bounds = JsonHelper::insert_at(root, "items", 10, "cherry");
    EXPECT_FALSE(fail_bounds) << "Should return false if index is larger than array size";
}

