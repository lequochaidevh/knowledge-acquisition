// Using declarations for clean and readable code
using json = nlohmann::json;

/**
 * @brief Component under test: A helper class to format user data into a JSON string.
 */
class UserProfileFormatter {
 public:
    static std::string make_json_profile(const std::string& name, int age, const std::vector<std::string>& skills) {
        // Construct a modern JSON object using nlohmann/json
        json j;
        j["name"]   = name;
        j["age"]    = age;
        j["skills"] = skills;

        // Serialize JSON to string and format it with a header using fmt
        std::string json_str = j.dump();
        return fmt::format("[USER_DATA] {}", json_str);
    }
};

