nlohmann::json generate_large_data(size_t count) {
    nlohmann::json root = nlohmann::json::array();

    for (size_t i = 0; i < count; ++i) {
        nlohmann::json user;
        user["id"]       = i + 1000;
        user["username"] = "user_" + std::to_string(i);
        user["meta"]     = {{"login_count", i * 5}, {"status", (i % 2 == 0) ? "active" : "offline"}};
        user["tags"]     = {"member", "dev_pool"};

        root.push_back(user);
    }

    return root;
}

